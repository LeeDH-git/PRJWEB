    PJT_JG_C          VARCHAR2(3)                  NOT NULL,
    CONSTRAINT PK_FL_EMP_ADMIN
        PRIMARY KEY (EMP_NO, EMP_EMAIL),
    CONSTRAINT FK_FL_EMP_ADMIN
        FOREIGN KEY (EMP_NO, EMP_EMAIL) REFERENCES FL_EMP
